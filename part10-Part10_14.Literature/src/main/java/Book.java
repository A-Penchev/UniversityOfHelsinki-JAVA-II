
public class Book {
    private String name;
    private int ageRec;
    
    public Book(String name,int ageRec){
        this.name=name;
        this.ageRec=ageRec;
    }
    
    public String getName(){
        return this.name;
    }
    
    public int getAgeRec(){
        return this.ageRec;
    }
    
}
